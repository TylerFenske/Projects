/**
 * Bubble object that contains only a String and a getter method for the String.
 * String is used to represent bubble on game board.
 * 
 * @author Tyler Fenske
 * @version 2016-12-06
 */

public class Bubble {

    private String bubble;

    public Bubble(String bubble) {
        this.bubble = bubble;
    }
    

    public String getBubble() {
        return bubble;
    }

    
    

}
